    return [UIColor colorWithRed:226.0f/255.0f green:28.0f/255.0f blue:119.0f/255.0f alpha:1.0f];
}

+ (UIColor *)pigRedColor {
    return [UIColor colorWithRed:252.0f/255.0f green:34.0f/255.0f blue:37.0f/255.0f alpha:1.0f];
}

@end
